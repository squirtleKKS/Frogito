package lang.lexer;

/**
 * Перечисление всех типов токенов, поддерживаемых языком <b>Frogito</b>.
 * <p>
 * Каждый элемент этого перечисления представляет собой конкретный тип
 * синтаксического элемента: ключевое слово, оператор, литерал, разделитель и т.д.
 * </p>
 *
 * <p>
 * Используется лексером (в разработке) и парсером (в разработке) для определения
 * значения и роли токена в программе.
 * </p>
 *
 * @see Token
 * @since 1.0
 */
public enum TokenType {

    /** Ключевое слово {@code var} — объявление переменной. */
    KW_VAR,
    /** Ключевое слово {@code func} — объявление функции. */
    KW_FUNC,
    /** Ключевое слово {@code return} — возврат значения из функции. */
    KW_RETURN,
    /** Ключевое слово {@code if} — начало условного оператора. */
    KW_IF,
    /** Ключевое слово {@code else} — ветка "иначе". */
    KW_ELSE,
    /** Ключевое слово {@code for} — цикл с параметрами. */
    KW_FOR,
    /** Ключевое слово {@code while} — цикл с условием. */
    KW_WHILE,
    /** Ключевое слово {@code break} — выход из цикла. */
    KW_BREAK,
    /** Ключевое слово {@code continue} — переход к следующей итерации цикла. */
    KW_CONTINUE,

    /** Тип {@code int} — целое число. */
    KW_INT,
    /** Тип {@code float} — число с плавающей точкой. */
    KW_FLOAT,
    /** Тип {@code bool} — логическое значение. */
    KW_BOOL,
    /** Тип {@code string} — строка. */
    KW_STRING,
    /** Тип {@code array} — массив. */
    KW_ARRAY,
    /** Тип {@code void} — отсутствие возвращаемого значения. */
    KW_VOID,

    /** Логическое значение {@code true}. */
    BOOL_TRUE,
    /** Логическое значение {@code false}. */
    BOOL_FALSE,

    /** Идентификатор (имя переменной, функции и т.д.). */
    IDENT,
    /** Целочисленный литерал. */
    INT_LITERAL,
    /** Число с плавающей точкой. */
    FLOAT_LITERAL,
    /** Строковый литерал в кавычках. */
    STRING_LITERAL,

    /** Оператор сложения {@code +}. */
    PLUS,
    /** Оператор вычитания {@code -}. */
    MINUS,
    /** Оператор умножения {@code *}. */
    STAR,
    /** Оператор деления {@code /}. */
    SLASH,
    /** Оператор остатка {@code %}. */
    PERCENT,

    /** Логическое И {@code &&}. */
    AND,
    /** Логическое ИЛИ {@code ||}. */
    OR,
    /** Логическое отрицание {@code !}. */
    NOT,

    /** Равно {@code ==}. */
    EQ,
    /** Не равно {@code !=}. */
    NEQ,
    /** Меньше {@code <}. */
    LT,
    /** Больше {@code >}. */
    GT,
    /** Меньше или равно {@code <=}. */
    LE,
    /** Больше или равно {@code >=}. */
    GE,
    /** Присваивание {@code =}. */
    ASSIGN,

    /** Левая круглая скобка {@code (}. */
    LPAREN,
    /** Правая круглая скобка {@code )}. */
    RPAREN,
    /** Левая фигурная скобка {@code &#123; }. */
    LBRACE,
    /** Правая фигурная скобка {@code &#125; }. */
    RBRACE,
    /** Левая квадратная скобка {@code [}. */
    LBRACK,
    /** Правая квадратная скобка {@code ]}. */
    RBRACK,
    /** Точка с запятой {@code ;}. */
    SEMICOLON,
    /** Запятая {@code ,}. */
    COMMA,

    /** Однострочный комментарий, начинающийся с {@code //}. */
    LINE_COMMENT,
    /** Конец файла (служебный токен). */
    EOF
}
