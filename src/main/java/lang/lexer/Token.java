package lang.lexer;


/**
 * Интерфейс, описывающий единицу лексического анализа — <b>токен</b>.
 * <p>
 * Токен — это минимальная осмысленная часть исходного кода:
 * ключевое слово, идентификатор, литерал, оператор или символ-разделитель.
 * </p>
 * <p>
 * Каждый токен имеет:
 * <ul>
 *     <li>тип ({@link TokenType}) — определяет, что это за элемент синтаксиса;</li>
 *     <li>лексему — исходный текст, из которого он был получен;</li>
 *     <li>позицию в файле (строку и колонку) — для отображения ошибок.</li>
 * </ul>
 *
 * @see TokenType
 * @since 1.0
 */
public interface Token {

    /**
     * Возвращает тип токена.
     *
     * @return тип токена ({@link TokenType})
     */
    TokenType getType();

    /**
     * Возвращает исходный текст (лексему), соответствующий токену.
     *
     * @return исходная строка токена
     */
    String getLexeme();

    /**
     * Возвращает номер строки, где начинается токен.
     * Нумерация строк начинается с 1.
     *
     * @return номер строки токена
     */
    int getLine();

    /**
     * Возвращает номер символа (колонку), где начинается токен.
     * Нумерация колонок начинается с 1.
     *
     * @return позиция токена в строке
     */
    int getColumn();
}
